const listeners = new Set<(lang: Lang) => void>();

let current: Lang = detectInitialLang();

export function setLang(l: Lang) {
  if (current === l) return;
  current = l;
  try { secureStorage.setItem('lang', l); } catch { }
  listeners.forEach((listener) => {
    try {
      listener(l);
    } catch { }
  });
}

export function getLang(): Lang {
  try {
    const stored = secureStorage.getItem<Lang>('lang');
    if (stored && SUPPORTED_LANGS.includes(stored)) {
      current = stored;
    }
  } catch { }
  return current;
}

export function t(key: string) { const lang = getLang(); return DICT[lang][key] || DICT.en[key] || key; }

export function useI18n() {
  const [langState, setLangState] = useState<Lang>(() => getLang());

  useEffect(() => {
    const listener = (nextLang: Lang) => setLangState(nextLang);
    listeners.add(listener);
    return () => { listeners.delete(listener); };
  }, []);

  useEffect(() => {
    if (langState !== current) {
      setLang(langState);
    }
  }, [langState]);

  const translate = useCallback((key: string) => DICT[langState][key] || DICT.en[key] || key, [langState]);

  const dict = useMemo(() => DICT[langState], [langState]);

  const setLanguage = useCallback((nextLang: Lang) => {
    setLang(nextLang);
  }, []);

  // Pluralization support for all languages
  const pluralize = useCallback((count: number, key: string): string => {
    const translation = DICT[langState][key] || DICT.en[key];

    if (!translation) return `${count} ${key}`;

    // Simple pluralization: use 'one' for count=1, 'other' for everything else
    // Most European languages follow this pattern
    const isPlural = count !== 1;
    const form = isPlural ? 'other' : 'one';

    // Check if translation is plural object (e.g., { one: "1 show", other: "{count} shows" })
    if (typeof translation === 'object' && translation !== null && form in translation) {
      const pluralObj = translation as Record<string, string>;
      const template = pluralObj[form];
      if (template) {
        return template.replace('{count}', String(count));
      }
    }

    return `${count} ${translation}`;
  }, [langState]);

  return {
    lang: langState,
    setLang: setLanguage,
    t: translate,
    pluralize,
    dict
  } as const;
}
